-- # Genero el script de creacion de la tabla apuesta
create table apuestas
(
  bet_id       int         identity(1,1)
 ,user_id      int         not null
 ,n1           varchar(30) null
 ,n2           varchar(30) null
 ,n3           varchar(30) null
 ,n4           varchar(30) null
 ,n5           varchar(30) null
 ,n6           varchar(30) null
 ,nr           varchar(30) null
 ,actual_date  date        null
 ,day_of_week  int         null
 ,bet_date     date        null
 ,price        numeric(8,2) null
 ,primary key(bet_id, user_id)
 ,foreign key (user_id) references [dbLoteria].[dbo].usuarios(id)
)
EXEC dbLoteria.sys.sp_addextendedproperty 'MS_Description', N'Fecha actual', 'schema', N'dbo', 'table', N'apuestas', 'column', N'actual_date'
EXEC dbLoteria.sys.sp_addextendedproperty 'MS_Description', N'Dia de la semana', 'schema', N'dbo', 'table', N'apuestas', 'column', N'day_of_week'
EXEC dbLoteria.sys.sp_addextendedproperty 'MS_Description', N'Fecha de apuesta', 'schema', N'dbo', 'table', N'apuestas', 'column', N'bet_date'
EXEC dbLoteria.sys.sp_addextendedproperty 'MS_Description', N'Monto de apuesta', 'schema', N'dbo', 'table', N'apuestas', 'column', N'price'
go
